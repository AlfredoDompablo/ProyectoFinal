    private void listaAereopuertosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listaAereopuertosMouseClicked
        // TODO add your handling code here:
        listaAereopuertos.getSelectedValue();
        Grafo g = ES.leerDeArchivo();
        nodoLista = g.getGrafo();
        Grafo.Nodo Encontrado;
        conexiones.removeAllElements();
        ArrayList<Grafo.Nodo> grafito = nodoLista.get(listaAereopuertos.getSelectedValue());
        conexionesLista.setEnabled(true);
        for (Grafo.Nodo nodo : grafito) {
            conexiones.addElement(nodo.getVertice());
        }
        if (conexiones.isEmpty()) {
            conexiones.addElement("No hay conexiones a otros aereopuertos");
            conexionesLista.setEnabled(false);
        }
    }//GEN-LAST:event_listaAereopuertosMouseClicked

